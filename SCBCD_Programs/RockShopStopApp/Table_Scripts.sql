CREATE TABLE USER_TABLE(USER_NAME VARCHAR(100) PRIMARY KEY NOT NULL,FIRST_NAME VARCHAR(100) NOT NULL,
LAST_NAME VARCHAR(100) NOT NULL,
USER_PASSWORD VARCHAR(100) NOT NULL,
USER_ADDRESS VARCHAR(300) NOT NULL,
USER_COUNTRY VARCHAR(50) NOT NULL,
USER_STATE VARCHAR(50) NOT NULL,
USER_CITY VARCHAR(50) NOT NULL,
USER_PINCODE VARCHAR(10) NOT NULL,
EMAIL_ID VARCHAR(100) NOT NULL,
CREDIT_PTS INTEGER DEFAULT 0);


CREATE TABLE CATEGORY_SD_TABLE(CATEGORY_ID INTEGER PRIMARY KEY NOT NULL,CATEGORY_NAME VARCHAR(100));

CREATE TABLE ITEM_SD_TABLE(ITEM_ID INTEGER PRIMARY KEY NOT NULL, CATEGORY_ID INTEGER, ITEM_NAME VARCHAR(100),ITEM_PRICE INTEGER,NOS_REMAINING INTEGER);
ALTER TABLE ITEM_SD_TABLE ADD FOREIGN KEY(CATEGORY_ID) REFERENCES CATEGORY_SD_TABLE(CATEGORY_ID);

CREATE TABLE ITEM_SD_TECHNICAL_DETAILS(ITEM_ID INTEGER PRIMARY KEY NOT NULL,ITEM_MAKE VARCHAR(50),ITEM_CAPACITY VARCHAR(50),ITEM_DESCRIPTION VARCHAR(1000), AVAIL_COLORS VARCHAR(100))
ALTER TABLE ITEM_SD_TECHNICAL_DETAILS ADD FOREIGN KEY(ITEM_ID) REFERENCES ITEM_SD_TABLE(ITEM_ID);

CREATE TABLE CART_ITEMS(TRANSACTION_ID INTEGER NOT NULL,CART_SER_NO INTEGER NOT NULL,USER_NAME VARCHAR(100),ITEM_ID INTEGER,ITEM_NAME VARCHAR(100),QUANTITY_REQD INTEGER,PRICE INTEGER);
ALTER TABLE CART_ITEMS ADD PRIMARY KEY(TRANSACTION_ID,CART_SER_NO);

CREATE TABLE TRANSACTION_HISTORY(TRANSACTION_ID INTEGER PRIMARY KEY NOT NULL, USER_NAME VARCHAR(100), ITEM_IDS TEXT, ITEM_NAMES TEXT, TRANSACTION_PRICE INTEGER, TRANS_TIMESTAMP TIMESTAMP);

